def spotlight(title: str, body: str) -> str:
    return (
        f'<details class="spotlight">'
        f'<summary class="lab">{title}</summary>'
        f'<div class="spotlight__body">{body}</div></details>'
    )


def set_day_spotlights(html: str, day: int, items: list[tuple[str, str]]) -> str:
    num = f"{day:02d}"
    block = "\n        ".join(spotlight(t, b) for t, b in items)
    pat = (
        rf'(<article class="day reveal">\s*'
        rf'<div class="day__rail"><div class="day__num serif">{num}<small>DAY</small></div>[\s\S]*?'
        rf'<p class="day__intro">[^<]*</p>\s*)'
        rf'(?:<details class="spotlight">[\s\S]*?</details>\s*)*'
        rf'(<ul class="plan">|<div class="note">)'
    )
    new_html, n = re.subn(pat, rf"\1{block}\n        \2", html, count=1)
    if n != 1:
        raise RuntimeError(f"Failed to patch spotlights for day {day} (matches={n})")
    return new_html


def main() -> None:
    html = HTML_PATH.read_text(encoding="utf-8")
    for day in sorted(DAY_SPOTLIGHTS):
        html = set_day_spotlights(html, day, DAY_SPOTLIGHTS[day])
        print(f"Day {day:02d}: {len(DAY_SPOTLIGHTS[day])} spotlights")
    HTML_PATH.write_text(html, encoding="utf-8")
    index = HTML_PATH.parent / "index.html"
    if index.exists():
        index.write_text(html, encoding="utf-8")
    print("Done.")


if __name__ == "__main__":
    main()
